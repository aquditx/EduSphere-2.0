function createTrack(labelLines) {
  const text = `WEBVTT\n\n00:00:00.000 --> 00:00:04.000\n${labelLines[0]}\n\n00:00:04.000 --> 00:00:08.000\n${labelLines[1]}\n\n00:00:08.000 --> 00:00:12.000\n${labelLines[2]}\n`;
  return `data:text/vtt;charset=utf-8,${encodeURIComponent(text)}`;
}


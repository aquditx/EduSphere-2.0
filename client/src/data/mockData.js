function buildCourse(config) {
  const {
    slug, title, subtitle, description,
    category, level, durationMinutes,
    price, ratingAverage, enrollmentCount, trendingScore,
    createdAt, updatedAt,
    thumbnailId, accentIdx,
    instructor,
    skills, language = "English",
    outcomes, modules: moduleSpecs,
  } = config;
  const ins = INSTRUCTORS[instructor];
  const modules = moduleSpecs.map((spec, mIdx) => ({
    id: `module-${slug}-${mIdx}`,
    title: spec.title,
    lessons: spec.lessons.map((lessonTitle, lIdx) => ({
      id: `lesson-${slug}-${mIdx}-${lIdx}`,
      title: lessonTitle,
      durationSeconds: 540 + mIdx * 180 + lIdx * 75,
      preview: mIdx === 0 && lIdx === 0,
      description: `Hands-on walkthrough of ${lessonTitle.toLowerCase()}.`,
      videoUrl: sampleVideo,
      transcriptUrl: createTrack([
        `Introduction to ${lessonTitle}.`,
        "Follow the guided steps in this lesson.",
        "Apply the technique in your own project.",
      ]),
    })),
  }));
  return {
    id: `course-${slug}`,
    slug,
    title,
    subtitle,
    description,
    category,
    level,
    durationMinutes,
    ratingAverage,
    ratingCount: Math.max(50, Math.floor(enrollmentCount / 12)),
    enrollmentCount,
    price,
    status: "approved",
    trendingScore,
    createdAt,
    updatedAt,
    thumbnail: unsplash(thumbnailId),
    accent: ACCENTS[accentIdx % ACCENTS.length],
    instructorId: ins.id,
    instructorName: ins.name,
    instructorHeadline: ins.headline,
    skills,
    language,
    modules,
    outcomes,
    reviews: [],
  };
}

